/**
 * 网络工具类型枚举
 *
 * @author 超悟空
 * @version 2.0 2016/8/4
 * @since 1.0
 */
public enum NetworkType {

    /**
     * http get类型请求
     */
    GET,
    /**
     * http post类型的请求
     */
    POST,
    /**
     * 下载
     */
    DOWNLOAD,
    /**
     * 上传
     */
    UPLOAD,
    /**
     * http delete类型的请求
     */
    DELETE,
    /**
     * http put类型的请求
     */
    PUT
}
